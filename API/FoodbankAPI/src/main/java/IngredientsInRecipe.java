/**
 * Class representing ingredients in recipe
 */

public class IngredientsInRecipe {

    private String ingredientName;
    private double unitsOfIngredient;
    private double ingredientPriceInRecipe;

    public String getIngredientName() {
        return ingredientName;
    }

    public void setIngredientName(String ingredientName) {
        this.ingredientName = ingredientName;
    }

    public double getUnitsOfIngredient() {
        return unitsOfIngredient;
    }

    public void setUnitsOfIngredient(double unitsOfIngredient) {
        this.unitsOfIngredient = unitsOfIngredient;
    }

    public double getIngredientPriceInRecipe() {
        return ingredientPriceInRecipe;
    }

    public void setIngredientPriceInRecipe(double ingredientPriceInRecipe) {
        this.ingredientPriceInRecipe = ingredientPriceInRecipe;
    }



}
